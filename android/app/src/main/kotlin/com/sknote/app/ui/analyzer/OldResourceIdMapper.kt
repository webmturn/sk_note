    fun getDrawableFromOldId(context: Context, oldIdStr: String): Int {
        val oldId = oldIdStr.toIntOrNull() ?: return R.drawable.ic_comp_default
        val name = idToName[oldId] ?: return R.drawable.ic_comp_default
        val resId = context.resources.getIdentifier(name, "drawable", context.packageName)
        return if (resId != 0) resId else R.drawable.ic_comp_default
    }

    fun isValidIconId(idString: String): Boolean {
        val id = idString.toIntOrNull() ?: return false
        return id in LOWEST_ID..HIGHEST_ID
    }

